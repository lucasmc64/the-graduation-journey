public class Triangulo {
    private float lado1;
    private float lado2;
    private float lado3;
    
    public void setLado1(float l1) {
        lado1 = l1;
    }
    
    public float getLado1() {
        return lado1;
    }
    
    public void setLado2(float l2) {
        lado2 = l2;
    }
    
    public float getLado2() {
        return lado2;
    }
    
    public void setLado3(float l3) {
        lado3 = l3;
    }
    
    public float getLado3() {
        return lado3;
    }
}
